import math

# sqrt(x)
print(math.sqrt(4)) # Output: 2.0

# ceil(x)
print(math.ceil(4.2)) # Output: 5

# floor(x)
print(math.floor(4.9)) # Output: 4

# exp(x)
print(math.exp(2)) # Output: 7.3890560989306495

# log(x)
print(math.log(10)) # Output: 2.302585092994046

# log10(x)
print(math.log10(100)) # Output: 2.0

# pow(x, y)
print(math.pow(2, 3)) # Output: 8.0

# sin(x)
print(math.sin(math.pi/2)) # Output: 1.0

# cos(x)
print(math.cos(math.pi/2)) # Output: 6.123233995736766e-17

# tan(x)
print(math.tan(math.pi/4)) # Output: 0.9999999999999999