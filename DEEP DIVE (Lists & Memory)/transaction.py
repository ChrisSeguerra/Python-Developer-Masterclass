# To fins the maximum, minimum, and sum of a List in Python, 
# we can use this built-in function min(), max(), and sum() respectivly.

transactions = [10.1, 22.0, 32.2, 17.8, 55.0]

minimum = min(transactions)
maximum = max(transactions)
total = sum(transactions)

print(f"The smallest transaction was {minimum}")
print(f"The largest transaction was {maximum}")
print(f"The largest transaction was {total:.2f}")

